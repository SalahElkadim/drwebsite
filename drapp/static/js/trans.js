function switchLanguage(lang) {
  localStorage.setItem("selectedLanguage", lang);
  const elements = document.querySelectorAll("[data-key]");
  const html = document.documentElement;

  elements.forEach((el) => {
    const key = el.getAttribute("data-key");

    // ุงุณุชุซูุงุก ุฃุฒุฑุงุฑ ุงููุบุฉ
    if (el.id === "en-btn" || el.id === "ar-btn") return;

    // ุงุฎุชูุงุฑ ุงููุต ุงูุฌุฏูุฏ ุญุณุจ ุงููุบุฉ
    const newText =
      lang === "en"
        ? allTranslations[key] || key
        : key;

    // ูู ุงูุนูุตุฑ ุนูุฏู class typewriter-text ูุนูุฏ ุงููุชุงุจุฉ ูู ุงูุจุฏุงูุฉ
    if (el.classList.contains("typewriter-text")) {
      if (el._typewriter && el._typewriter.cancel) el._typewriter.cancel();
      startTypewriter(el, newText, 40);
    } else {
      // ุบูุฑ ูุฏู ุบููุฑ ุงููุต ูุจุงุดุฑุฉ
      el.innerHTML = newText;
    }
  });

  // ุชุบููุฑ ุงูุงุชุฌุงู (RTL/LTR)
  html.lang = lang;
  html.dir = lang === "en" ? "ltr" : "rtl";
  document.body.classList.toggle("ltr", lang === "en");
  document.body.classList.toggle("rtl", lang === "ar");

  // ุถุจุท ุงููุญุงุฐุงุฉ ุงูุชููุงุฆูุฉ
  const alignables = document.querySelectorAll(".auto-align, [data-key]");
  alignables.forEach((el) => {
    el.style.textAlign = html.dir === "ltr" ? "left" : "right";
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      el.style.direction = html.dir;
    }
  });
}

