function startTypewriter(element, text, speed = 50) {
  // ูู ูู ูุชุงุจุฉ ุฌุงุฑูุฉ ูููููุง ุฃููุงู
  if (element._typewriter && element._typewriter.cancel) {
    element._typewriter.cancel();
  }

  element.textContent = ""; // ุงูุฑุงุบ ุงููุต ููุฑุงู
  element.style.borderLeft = "3px solid white";

  let i = 0;
  let timerId = null;

  function type() {
    if (i < text.length) {
      element.textContent += text.charAt(i);
      i++;
      timerId = setTimeout(type, speed);
    } else {
      // ุจุนุฏ ูุง ูุฎูุต ุงููุชุงุจุฉ ูุดูู ุงููุคุดุฑ ุจุนุฏ ุซุงููุฉ
      timerId = setTimeout(() => {
        element.style.borderLeft = "none";
        delete element._typewriter;
      }, 1000);
    }
  }

  element._typewriter = {
    cancel: function () {
      if (this.timer) clearTimeout(this.timer);
      element.style.borderLeft = "none";
      delete element._typewriter;
    },
    timer: timerId,
  };

  type();
  return element._typewriter;
}

